#!/usr/bin/env python3
"""Generate a SystemVerilog fallback disassembler for vector reserved encodings.

The emitted function `disasm_fallback_vector` maps a 32-bit insn back to its
canonical mnemonic when the live simulator's disassembler returns "illegal" for
an encoding the spec marks reserved. Extension covergroups can call it from
`save_rvvi_data` when `inst_name == "illegal"` so reserved-encoding tests still
dispatch to the correct covergroup.

Families below are extension-agnostic — any extension that needs to sample
reserved vector encodings can reuse the same fallback. Add new families here
to extend coverage.

Output: framework/src/act/fcov/coverage/RISCV_disasm_fallback.svh
"""

from __future__ import annotations

import re
from pathlib import Path

REPO = Path(__file__).resolve().parents[4]
ENC_H = REPO / "tests" / "env" / "encoding.h"
DECODE_PKG = REPO / "framework" / "src" / "act" / "fcov" / "coverage" / "RISCV_decode_pkg.svh"
IMPORTED_DECODE_PKG = REPO / "framework" / "src" / "act" / "fcov" / "coverage" / "RISCV_imported_decode_pkg.svh"
OUT = REPO / "framework" / "src" / "act" / "fcov" / "coverage" / "RISCV_disasm_fallback.svh"


def parse_matches() -> dict[str, tuple[int, int]]:
    matches: dict[str, int] = {}
    masks: dict[str, int] = {}
    pat_m = re.compile(r"^#define MATCH_(\S+)\s+0x([0-9a-fA-F]+)")
    pat_k = re.compile(r"^#define MASK_(\S+)\s+0x([0-9a-fA-F]+)")
    with ENC_H.open() as f:
        for line in f:
            m = pat_m.match(line)
            if m:
                matches[m.group(1)] = int(m.group(2), 16)
                continue
            m = pat_k.match(line)
            if m:
                masks[m.group(1)] = int(m.group(2), 16)
    return {k: (matches[k], masks[k]) for k in matches if k in masks}


ENC = parse_matches()


def name_to_mnem(name: str) -> str:
    """VADC_VVM -> vadc.vvm, VLE8_V -> vle8.v"""
    return name.lower().replace("_", ".")


def to_casez(match: int, mask: int, relax: int = 0) -> str:
    """Build a 32-bit casez pattern. Bits in mask are constrained,
    bits cleared by `relax` are forced to '?' even if originally in mask."""
    eff_mask = mask & ~relax
    bits = []
    for i in range(31, -1, -1):
        if (eff_mask >> i) & 1:
            bits.append(str((match >> i) & 1))
        else:
            bits.append("?")
    s = "".join(bits)
    return f"32'b{s[0:6]}_{s[6]}_{s[7:12]}_{s[12:17]}_{s[17:20]}_{s[20:25]}_{s[25:32]}"


# Bit-mask constants (bit positions)
VM = 1 << 25
VS2 = 0x1F << 20
VS1 = 0x1F << 15  # rs1 / vs1 / fs1 / fd field (bits 19:15)
RS1 = VS1
VD = 0x1F << 7
NF = 0x7 << 29
MEW = 1 << 28


# Families: instructions whose reserved bits must be relaxed so reserved-encoding
# tests still dispatch to the canonical mnemonic. Extend as new extensions add
# reserved-encoding coverage needs.
ARITH_VM_RELAX = [
    "VADC_VVM",
    "VADC_VXM",
    "VADC_VIM",
    "VSBC_VVM",
    "VSBC_VXM",
]

MASK_LOGICAL_VM_RELAX = [
    "VMAND_MM",
    "VMNAND_MM",
    "VMANDN_MM",
    "VMOR_MM",
    "VMNOR_MM",
    "VMORN_MM",
    "VMXOR_MM",
    "VMXNOR_MM",
]

VMV_VS2_RELAX = [
    "VMV_V_V",
    "VMV_V_X",
    "VMV_V_I",
    "VFMV_V_F",
    "VID_V",
]

VMVXS_VM_RELAX = [
    "VMV_X_S",
    "VMV_S_X",
    "VFMV_F_S",
    "VFMV_S_F",
]

VCOMPRESS_VM_RELAX = [
    "VCOMPRESS_VM",
]


def emit() -> None:
    lines = []
    lines.append("// Auto-generated by generators/coverage/src/covergroupgen/build_disasm_fallback.py")
    lines.append("// Fallback disassembler for reserved vector encodings.")
    lines.append("// Returns the canonical mnemonic for encodings the live simulator rejects as illegal.")
    lines.append('// Extension-agnostic: any covergroup can call this when inst_name == "illegal".')
    lines.append("function string disasm_fallback_vector(input bit [31:0] insn);")
    lines.append("  casez (insn)")

    def add(name: str, relax: int, mnem_override: str = "") -> None:
        if name not in ENC:
            print(f"WARN: missing {name}")
            return
        m, k = ENC[name]
        mnem = mnem_override or name_to_mnem(name)
        pat = to_casez(m, k, relax)
        lines.append(f'    {pat}: return "{mnem}";')

    lines.append("    // vadc/vsbc/vmadc/vmsbc: relax vm bit (reserved when vm=1 for masked-only forms)")
    for n in ARITH_VM_RELAX:
        add(n, VM)

    lines.append("    // mask logical: relax vm bit (reserved when vm=0)")
    for n in MASK_LOGICAL_VM_RELAX:
        add(n, VM)

    lines.append("    // vmv.v.*/vid.v: relax vs2 (reserved when vs2!=0)")
    for n in VMV_VS2_RELAX:
        add(n, VS2)

    lines.append("    // vmv.x.s/vfmv.f.s etc.: relax vm + reserved-zero scalar field for cg sampling")
    for n in ("VMV_X_S", "VFMV_F_S"):
        add(n, VM | RS1)
    for n in ("VMV_S_X", "VFMV_S_F"):
        add(n, VM | VS2)

    lines.append("    // vcompress.vm: relax vm bit (reserved when vm=0)")
    for n in VCOMPRESS_VM_RELAX:
        add(n, VM)

    lines.append("    // vmv<nr>r.v: relax vs1 (simm); always returns vmv1r.v for the cg dispatch")
    add("VMV1R_V", VS1, mnem_override="vmv1r.v")

    lines.append("    // vs2r/vs4r/vs8r.v: relax vd bits + mew so reserved encodings dispatch correctly")
    add("VS2R_V", VD | MEW, mnem_override="vs2r.v")
    add("VS4R_V", VD | MEW, mnem_override="vs4r.v")
    add("VS8R_V", VD | MEW, mnem_override="vs8r.v")
    lines.append("    // vs1r.v catch-all: relax nf + mew; reserved encodings dispatch to vs1r.v")
    add("VS1R_V", NF | MEW, mnem_override="vs1r.v")

    lines.append("    // vector LS with mew=1 (reserved): map back to canonical mnemonic for cg dispatch")
    pat_decode = re.compile(r"localparam \[31:0\] (\S+)\s*=\s*32'b([01?]{32});")
    seen = set()
    decode_lines = DECODE_PKG.read_text().splitlines() + IMPORTED_DECODE_PKG.read_text().splitlines()
    mnem_alias = {"VLE1_V": "vlm.v", "VSE1_V": "vsm.v"}
    skip_names = {"VL1R_V", "VL2R_V", "VL4R_V", "VL8R_V"}
    for line in decode_lines:
        m = pat_decode.search(line)
        if not m:
            continue
        name = m.group(1)
        bits = list(m.group(2))
        op = "".join(bits[25:32])
        if op not in ("0000111", "0100111"):
            continue
        if name.startswith("VS") and "R_V" in name:
            continue
        if name.startswith("VL") and "RE" in name:
            continue
        if name in skip_names:
            continue
        if bits[3] == "0":
            bits[3] = "1"
        elif bits[3] != "1":
            continue
        s = "".join(bits)
        pat = f"32'b{s[0:6]}_{s[6]}_{s[7:12]}_{s[12:17]}_{s[17:20]}_{s[20:25]}_{s[25:32]}"
        if pat in seen:
            continue
        seen.add(pat)
        mnem = mnem_alias.get(name, name_to_mnem(name))
        lines.append(f'    {pat}: return "{mnem}";')

    lines.append('    default: return "illegal";')
    lines.append("  endcase")
    lines.append("endfunction")
    OUT.write_text("\n".join(lines) + "\n")
    print(f"Wrote {OUT} ({len(lines)} lines)")


if __name__ == "__main__":
    emit()
